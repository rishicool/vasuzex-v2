/**
 * Application Configuration
 * 
 * This file contains core application settings.
 * Uses env() helper to read from .env file.
 */

/**
 * Helper function to read environment variables
 */
function env(key, defaultValue = null) {
  return process.env[key] !== undefined ? process.env[key] : defaultValue;
}

module.exports = {
  /*
  |--------------------------------------------------------------------------
  | Application Name
  |--------------------------------------------------------------------------
  */
  name: env('APP_NAME', 'Neastore'),

  /*
  |--------------------------------------------------------------------------
  | Application Environment
  |--------------------------------------------------------------------------
  */
  env: env('NODE_ENV', 'local'),

  /*
  |--------------------------------------------------------------------------
  | Application Debug Mode
  |--------------------------------------------------------------------------
  */
  debug: env('APP_DEBUG', 'true') === 'true',

  /*
  |--------------------------------------------------------------------------
  | Application URL
  |--------------------------------------------------------------------------
  */
  url: env('APP_URL', 'http://localhost'),

  /*
  |--------------------------------------------------------------------------
  | Application Timezone
  |--------------------------------------------------------------------------
  */
  timezone: env('APP_TIMEZONE', 'UTC'),

  /*
  |--------------------------------------------------------------------------
  | Application Locale
  |--------------------------------------------------------------------------
  */
  locale: env('APP_LOCALE', 'en'),

  /*
  |--------------------------------------------------------------------------
  | Encryption Key
  |--------------------------------------------------------------------------
  */
  key: env('APP_KEY', ''),

  /*
  |--------------------------------------------------------------------------
  | API Prefix
  |--------------------------------------------------------------------------
  */
  api_prefix: env('API_PREFIX', '/api'),

  /*
  |--------------------------------------------------------------------------
  | CORS Origins
  |--------------------------------------------------------------------------
  */
  cors_origins: env('CORS_ORIGINS', '').split(',').filter(Boolean),
};
